    public void actualizarSesiones(){
        List<Sesion> sesiones = entrenadorActivo.getSesiones();
        sesiones.sort(Comparator.comparingInt(s -> ordenDias.get(s.getDia().toLowerCase())));
        modelo.setNumRows(0);
        for (Sesion sesion : sesiones) {
                modelo.addRow(new Object[]{sesion.getDia(), sesion.getNombre(), sesion.getHoraIni() + "-" + sesion.getHoraFin(), sesion.getCodigo()});
        }  
    }

