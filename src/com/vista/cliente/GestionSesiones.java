    private void txtBuscarSesionKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscarSesionKeyPressed
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            if(!txtBuscarSesion.getText().isBlank() || txtBuscarSesion.getText().isEmpty()){
                String buscarString = (String) txtBuscarSesion.getText();
                if(buscarString.replace(" ", "").matches("[a-zA-Z]+")){
                    if(gymPrincipal.buscarSesionNombre(buscarString)){
                        for(int i = 0; i<modelo.getRowCount(); i++){
                            if(((String) modelo.getValueAt(i, 1)).equalsIgnoreCase(buscarString)){
                                tblSesiones.setRowSelectionInterval(i, i);
                                txtBuscarSesion.setText("");
                                break;
                            }
                        }
                    }
                    else{
                        JOptionPane.showMessageDialog(this, "No se encontro una sesión con ese nombre"); 
                    }
                }
                else if(buscarString.matches("\\d+")){
                    int buscarInt = Integer.parseInt(buscarString);
                    if(gymPrincipal.buscarSesionCodigo(buscarInt)){
                        for(int i = 0; i<modelo.getRowCount(); i++){
                            if((int) modelo.getValueAt(i, 3) == buscarInt){
                                tblSesiones.setRowSelectionInterval(i, i);
                                txtBuscarSesion.setText("");
                                break;
                            }
                        }
                    }
                    else{
                        JOptionPane.showMessageDialog(this, "No se encontro una sesión con ese código"); 
                    }
                }
                else{
                    JOptionPane.showMessageDialog(this, "Debe buscar un código o un nombre!", "ADVERTENCIA", JOptionPane.WARNING_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_txtBuscarSesionKeyPressed


