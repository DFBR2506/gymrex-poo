    private void btnEliminarSesionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarSesionActionPerformed
        filaCambiar = tblSesiones.getSelectedRow();
        if(filaCambiar != -1){
            int codigoSesion = (int) tblSesiones.getValueAt(filaCambiar, 3);
            int opcion = JOptionPane.showConfirmDialog(this, "LA SESIÓN SE ELIMINARA DE FORMA PERMANENTE, DESEA CONTINUAR?", "Confirmación", JOptionPane.YES_NO_OPTION);
            if(opcion == JOptionPane.YES_OPTION){
                gymPrincipal.eliminarSesionCodigo(codigoSesion);
                entrenadorActivo.eliminarSesion(codigoSesion);
                modelo.removeRow(filaCambiar);
                JOptionPane.showMessageDialog(this, "SESIÓN ELIMINADA CON EXITO");
                actualizarSesiones();
                tblSesiones.repaint();
            }
        }
    }//GEN-LAST:event_btnEliminarSesionActionPerformed

    private void btnDetallesSesionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetallesSesionActionPerformed
        filaCambiar = tblSesiones.getSelectedRow();
        if(filaCambiar != -1){
            int codigoSesion = Integer.parseInt(tblSesiones.getValueAt(filaCambiar, 3).toString());
            List<Sesion> sesiones = gymPrincipal.getSesiones();
            for(Sesion sesion : sesiones){
                if(sesion.getCodigo()==codigoSesion){
                    JOptionPane.showMessageDialog(this, "Integrantes: " + "\n" + sesion.listarNombreIntegrantes() + "\n" + "Cantidad de integrantes: " + sesion.getIntegrantes().size());
                }
            }
        }
    }//GEN-LAST:event_btnDetallesSesionActionPerformed

