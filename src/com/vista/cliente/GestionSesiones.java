    public void actualizarSesiones(){
        List<Sesion> sesiones = gymPrincipal.getSesiones();
        sesiones.sort(Comparator.comparingInt(s -> ordenDias.get(s.getDia().toLowerCase())));
        modelo.setNumRows(0);
        for (Sesion sesion : sesiones) {
            if(sesion.buscarIntegrante(usuarioActivo.getCedula())){
                modelo.addRow(new Object[]{sesion.getDia(), sesion.getNombre(), sesion.getHoraIni() + "-" + sesion.getHoraFin(), sesion.getCodigo(), "Si"});
            }
            else{
                modelo.addRow(new Object[]{sesion.getDia(), sesion.getNombre(), sesion.getHoraIni() + "-" + sesion.getHoraFin(), sesion.getCodigo(), "No"});
            }
        }  
    }

