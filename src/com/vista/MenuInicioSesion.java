    public void cargarGimnasio(){
        ArchiGym persistenciaGym = new ArchiGym();
        if(new File("gimnasio.obj").exists()){
            try {
                gymRex = persistenciaGym.cargar();
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(this, "NO SE PUDIERON CARGAR LOS DATOS DEL GIMNASIO");
            } catch (ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(this, "NO SE PUDIERON CARGAR LOS DATOS DEL GIMNASIO");
            }
        }
        else{
            gymRex = new Gimnasio(102, "Gym Rex");
        }
    }

