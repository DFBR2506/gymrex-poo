    private void btnFormatearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFormatearActionPerformed
        File file = new File("gimnasio.obj");
        if(file.exists()){
            int opcion = JOptionPane.showConfirmDialog(this, "ESTA SEGURO DE FORMATEAR EL GIMNASIO? TODOS LOS DATOS SE PERDERAN", "Formateo", JOptionPane.YES_NO_OPTION);
            if(opcion == JOptionPane.YES_OPTION){
                if(file.delete()){
                    JOptionPane.showMessageDialog(this, "SE HA BORRADO EL ARCHIVO CON EXITO");
                }
                else{
                    JOptionPane.showMessageDialog(this, "NO SE PUDO BORRAR EL ARCHIVO");
                }
            }
        }
        else{
            JOptionPane.showMessageDialog(this, "NO HAY DATOS QUE FORMATEAR");
        }
        
        gymRex = new Gimnasio(102, "Gym Rex");
        inicioSesion = new InicioSesion(this, gymRex);
        registro = new Registro(gymRex);
        cambiarPanelSesion(inicioSesion);
    }//GEN-LAST:event_btnFormatearActionPerformed

